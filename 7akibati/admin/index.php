          $(document).on("click", ".delete-btn", function() {
    var id_commende = $(this).data("id");
    var nombre = $(this).data("nombre"); // Get the 'nombre' value

    var row = $(this).closest("tr");

    // AJAX request to delete project record
    $.ajax({
        url: "../backends/delete_commende.php",
        type: "POST",
        data: { id_commende: id_commende, nombre: nombre }, // Pass both values
        success: function(response) {
            // Remove the table row on successful deletion
            row.remove();
            console.log(response);
        },
        error: function(xhr, status, error) {
            console.log("AJAX Error: " + status + " - " + error);
        }
    });
});


